#include "calc.h"
/**
 * op_add - add two ints
 * @a: int number 1
 * @b: int number 2
 * Return: Sum of the two nums
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subs two nums
 * @a: num 1
 * @b: num 2
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul two nums
 * @a: num 1
 * @b: num 2
 * Return: the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div @a by @b
 * @a: num 1
 * @b: num 2
 * Return: the division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod @a by @b
 * @a: num 1
 * @b: num 2
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
